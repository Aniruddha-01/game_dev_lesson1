#1d lists

fruits = ["apple", "banana", "pineapple"]

#Check length of list - len function
print(len(fruits))
#Adding values - append functions
fruits.append("kiwi")

#Adding values - insert function
fruits.insert(1, "strawberry")

#Removing values - remove function
fruits.remove("apple")

#2d lists
matrix = [[1,2,3] , [4,5,6] , [7,8,9]]

#number of rows - len function
print(len(matrix))

#number of columns
print(len(matrix[1]))